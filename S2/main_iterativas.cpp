#include <iostream>
using namespace std;


int main() {
    int i;

    cout<<"Practicando while:\n";
    i=1;
    while (i<8){
        cout<<"UTEC\n";
        i++;
    }

    cout<<"\nPracticando for:\n";
    for(i=1;i<8;i++){
        cout<<"UTEC\n";
    }

    cout<<"\nPracticando do while:\n";
    i=1;
    do{
        cout<<"UTEC\n";
        i++;

    } while (i<8);




    return 0;
}
